def fetch_transcript_decentralized(video_id: str):
    # ì „ ì„¸ê³„ì— êµ¬ì¶•ëœ Piped ì˜¤í”ˆì†ŒìŠ¤ ìš°íšŒ ë…¸ë“œ ë¦¬ìŠ¤íŠ¸
    instances = [
        "https://pipedapi.kavin.rocks",
        "https://pipedapi.adminforge.de",
        "https://api.piped.projectsegfau.lt",
        "https://pipedapi.smarthome-bielefeld.de"
    ]
    
    for instance in instances:
        try:
            print(f"ğŸŒ ë¶„ì‚°í˜• ë…¸ë“œ ìš°íšŒ ì‹œë„ ì¤‘: {instance}")
            # ìš°íšŒ ë…¸ë“œì— ì˜ìƒ ë°ì´í„° ìš”ì²­
            res = requests.get(f"{instance}/streams/{video_id}", timeout=10)
            if res.status_code != 200:
                continue
            
            data = res.json()
            subtitles = data.get('subtitles', [])
            if not subtitles:
                continue
            
            # ìš°ì„ ìˆœìœ„: ì˜ì–´(ìˆ˜ë™) -> ì˜ì–´(ìë™) -> í•œêµ­ì–´ -> ì²« ë²ˆì§¸ ìë§‰
            target = next((s for s in subtitles if s.get('code') == 'en' and not s.get('autoGenerated')), None)
            if not target: target = next((s for s in subtitles if s.get('code') == 'en'), None)
            if not target: target = next((s for s in subtitles if s.get('code') == 'ko'), None)
            if not target: target = subtitles[0]
                
            sub_url = target.get('url')
            if not sub_url:
                continue
                
            print(f"âœ… ìë§‰ URL í™•ë³´ ì„±ê³µ ({instance}). ìµëª… ë‹¤ìš´ë¡œë“œ ì§„í–‰ ì¤‘...")
            sub_res = requests.get(sub_url, timeout=10)
            sub_text = sub_res.text
            
            parsed_data = []
            
            # 1. XML í¬ë§· íŒŒì‹±
            if sub_text.strip().startswith('<?xml') or sub_text.strip().startswith('<transcript'):
                root = ET.fromstring(sub_text)
                for child in root:
                    if child.tag == 'text':
                        start = float(child.attrib.get('start', 0))
                        text_content = child.text
                        if text_content:
                            text_content = text_content.replace('&amp;', '&').replace('&#39;', "'").replace('&quot;', '"')
                            parsed_data.append({'start': start, 'text': text_content})
            
            # 2. JSON3 í¬ë§· íŒŒì‹± (ìœ íŠœë¸Œ ê¸°ë³¸)
            elif sub_text.strip().startswith('{'):
                json_data = json.loads(sub_text)
                for event in json_data.get('events', []):
                    if 'segs' in event:
                        text_content = "".join([seg.get('utf8', '') for seg in event['segs']]).replace('\n', ' ').strip()
                        if text_content:
                            parsed_data.append({
                                'start': event.get('tStartMs', 0) / 1000.0,
                                'text': text_content
                            })
                            
            # 3. WebVTT í¬ë§· íŒŒì‹±
            elif "WEBVTT" in sub_text:
                blocks = sub_text.split('\n\n')
                for block in blocks:
                    lines = block.split('\n')
                    if len(lines) >= 2 and '-->' in lines[0]:
                        time_str = lines[0].split('-->')[0].strip()
                        parts = time_str.split(':')
                        if len(parts) == 3:
                            start = float(parts[0])*3600 + float(parts[1])*60 + float(parts[2])
                            text = " ".join(lines[1:]).strip()
                            if text: parsed_data.append({'start': start, 'text': text})
                            
            if parsed_data:
                return parsed_data
                
        except Exception as e:
            print(f"âš ï¸ ë…¸ë“œ ì—°ê²° ì‹¤íŒ¨ ({instance}): {e}")
            continue
            
    raise Exception("ëª¨ë“  ê¸€ë¡œë²Œ ìš°íšŒ ë…¸ë“œê°€ ìœ íŠœë¸Œ ë°©í™”ë²½ì— ì˜í•´ ì°¨ë‹¨ë˜ì—ˆìŠµë‹ˆë‹¤.")

